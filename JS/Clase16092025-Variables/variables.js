/* Estos operadores se usan para asignar un valor a una variable. El más común es el signo igual (=).

= Asignación simple: Asigna el valor del lado derecho a la variable del lado izquierdo.

let x = 10;
+= Adición y asignación: Suma el valor y asigna el resultado a la variable. Es una forma corta de escribir x = x + 5;.

let x = 10;
x += 5; // x ahora es 15
-= Sustracción y asignación: Resta el valor y asigna el resultado.

let y = 20;
y -= 5; // y ahora es 15
*= Multiplicación y asignación: Multiplica el valor y asigna el resultado.

let z = 3;
z *= 4; // z ahora es 12
/= División y asignación: Divide el valor y asigna el resultado.

let a = 10;
a /= 2; // a ahora es 5 */

